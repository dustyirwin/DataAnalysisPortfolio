# ‚ïî‚ïê‚ï° f67cd69b-5514-4ad0-a646-2751afb74129
function clean_duration(df::DataFrame)
	df.Duration = lowercase.(coalesce.(df.Duration, ""))
	df.Duration = replace.(df.Duration, 
	  "one"=>"1","two"=>"2","three"=>"3","four"=>"4","five"=>"5","six"=>"6","seven"=>"7","eight"=>"8","nine"=>"9","ten"=>"10",
	  "twenty"=>"20","thirty"=>"30","forty"=>"40","fifty"=>"50","sixty"=>"60","seventy"=>"70","eighty"=>"80","ninety"=>"90",
	  )
	
	df.Duration = let
	  v = []
	
	  for i in df.Duration
	
	    if occursin("sec", i) && (match(r"\d+", i) != nothing)
	      si = match(r"\d+", i).match
	      j = Second(parse(Int, si)) 
	      push!(v,j)
	    
	    elseif occursin("min", i) && (match(r"\d+", i) != nothing)
	      mi = match(r"\d+", i).match
	      j = Minute(parse(Int, mi))
	      push!(v, j)
	
	    elseif occursin("hour", i) && (match(r"\d+", i) != nothing)
	      hi = match(r"\d+", i).match
	      j = Hour(parse(Int, hi))
	      push!(v, j)
	
	    elseif occursin("hr", i) && (match(r"\d+", i) != nothing)
	      hi = match(r"\d+", i).match
	      j = Hour(parse(Int, hi))
	      push!(v, j)
	
	    else
	      push!(v, i == "" ? missing : i)
	    end
	  end 
	  
	  v
	end

	df
end

# ‚ïî‚ïê‚ï° b994237e-6b48-4e23-9b4d-44b0f55ae0e7
cleaned_ufos = let
	# dropping unused vars
	df = select(raw_ufos, Not([:Column1]))

	# restricting to USA states and territories
	df = @subset(df, :Country .== "USA")
	
	# converting datetime to DateTime objects
	df.datetime = Time.(df.datetime, dateformat"yyyy-mm-dd HH:MM:SS")
	
	# cleaning Posted date
	df.Posted = let
		ds = []
		for d in df.Posted
			
			if typeof(d) <: Missing
				push!(ds, d)
				continue
			else
				sd = split(d,"/")
			end
			
			if parse(Int, sd[3]) < 23
				sd[3] = "20" * sd[3]
			else
				sd[3] = "19" * sd[3]
			end
		
			push!(ds, Date(join(sd,"/"), dateformat"m/d/y"))
		end
		
		ds
	end
	
	# lowercasing & coalescing vars
	df.Country = uppercase.(coalesce.(df.Country,""))
	df.City = titlecase.(coalesce.(df.City, ""))
	df.State = uppercase.(coalesce.(df.State,""))
	
	# cleaning Duration var
	df = clean_duration(df)
	
	# cleaning State var
	df = @subset(df, :State .‚àâ [
		uppercase.(["Guam", "M0","Quintana Roo","Ontario","Cornwall","United States Virgin Islands","SK","ON","NS","NB","MB","BC","AB","Washington, Dc","","American Samoa"])
	]) 

	rename!(df, :City=>:city, :Posted=>:date, :datetime=>:time)
	
	df.city_state = df.city .* ", " .* df.State

	var_order = [:date, :time, :city] 
	df = select(df, var_order, Not(var_order))

	df = @subset(df, :date .> Date(2002,1,1))
	sort(df, :date)
end

