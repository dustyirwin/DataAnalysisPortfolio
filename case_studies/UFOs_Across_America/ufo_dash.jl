features = let
	df = select(us_ufos, [:date, :state_id])
	dropmissing!(df, [:date, :state_id])
	
	df.yr = year.(df.date)
	df.mth = month.(df.date)

	grp = groupby(df, [:yr,:mth,:state_id])
	df = combine(grp, :state_id=>length)
	rename!(df, :state_id_length=>:reported_sightings)

	# calculating weeks with no reports
	yrs = 2002:2022 |> collect
	mths = 1:12 |> collect
	states = df.state_id |> unique |> sort |> collect
	
	sl = SpinLock()
	_df = DataFrame(yr=[],mth=[],state_id=[],reported_sightings=[])
	
	@threads for sid in states

		for yr in yrs,
			mth in mths
			
			s = @subset(df, :state_id .== sid)
			s = filter(x-> x.yr == yr && x.mth == mth, s)
			
			if isempty(s)
				lock(sl)
				push!(_df, [yr,mth,sid,0])
				unlock(sl)
			end

		end
	end
	
	df = vcat(df, _df)

	# adding movies data
	df = leftjoin(df, us_movies_timeline, on=[:yr,:mth])

	# adding national beer, wine and spirits sales (in millions USD)
	df = leftjoin(df, beer_wine_spirits, on=[:yr,:mth])
	
	sort!(df, [:yr,:mth,:state_id])
	select(df,:reported_sightings, Not([:reported_sightings]))

	df.state_id = df.state_id
	df.yr = Int64.(df.yr)
	df.mth = Int64.(df.mth)
	df.reported_sightings = Int64.(df.reported_sightings)
	df.IADBOS_sum = ceil.(Int,(df.IADBOS_sum))
	df.BWS_sales = Int64.(df.BWS_sales)
	df.recession = Bool.(df.recession)
	
	coerce(df, 
		:reported_sightings => Continuous,
		:yr => OrderedFactor,
		:mth => OrderedFactor,
		#:state_id => Categorical,
		:recession => Binary,
	)
	
	df
end

